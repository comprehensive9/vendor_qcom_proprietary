////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
VOID* OsUtils::MallocAligned(
    SIZE_T size,
    SIZE_T alignment)
{
    VOID* pMem = NULL;
    bool isPowerOfTwo = ((alignment == 0) ? FALSE : (((alignment & (alignment - 1)) == 0) ? TRUE : FALSE));

    if (TRUE == isPowerOfTwo)
    {
        pMem = _aligned_malloc(size, alignment);
    }

    return pMem;
}

